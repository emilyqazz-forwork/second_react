export function loadPreferences() {
    const raw = JSON.parse(localStorage.getItem('chickodePrefs') || '{}');
    return {
        theme: raw.theme ?? 'light',
        lang: raw.lang ?? 'ko',
        bgm: raw.bgm ?? true,
        bgmTrack: raw.bgmTrack ?? 'cabin',
        persona: raw.persona ?? 'default',
    };
}

export function savePreferences(prefs) {
    localStorage.setItem('chickodePrefs', JSON.stringify(prefs));
}

export function useI18n() {
    const [prefs, setPrefs] = useState(loadPreferences());

    useEffect(() => {
        savePreferences(prefs);
    }, [prefs]);

    useEffect(() => {
        if (prefs.theme === 'dark') {
            document.body.classList.add('dark-mode');
            document.documentElement.classList.add('dark-mode');
        } else {
            document.body.classList.remove('dark-mode');
            document.documentElement.classList.remove('dark-mode');
        }
    }, [prefs.theme]);

    const t = (key) => {
        const texts = translations[prefs.lang] || translations['ko'];
        return texts[key] || key;
    };

    return { params: prefs, setParams: setPrefs, t };
}